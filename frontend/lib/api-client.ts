const API_BASE_URL = process.env.NEXT_PUBLIC_API_URL || "http://localhost:4000"

async function fetchJson<T>(url: string, options?: RequestInit): Promise<T> {
  let token: string | null = null
  if (typeof window !== "undefined") {
    token = localStorage.getItem("auth_token")
  }

  const res = await fetch(url, {
    ...options,
    headers: {
      "Content-Type": "application/json",
      ...(token ? { Authorization: `Bearer ${token}` } : {}),
      ...(options?.headers || {}),
    },
  })
  if (!res.ok) {
    throw new Error(`API error: ${res.statusText} (${res.status})`)
  }
  const body = await res.json()
  if (!body.success) {
    throw new Error(body.error || "API call failed")
  }
  return body.data as T
}

export async function fetchDashboardMetrics(): Promise<DashboardMetrics> {
  try {
    const stats = await fetchJson<{
      total: number
      bySeverity: Record<string, number>
      bySource: Record<string, number>
      byStatus: Record<string, number>
    }>(`${API_BASE_URL}/api/events/stats`)

    const criticalAlerts = stats.bySeverity.critical ?? 0
    const openAnomalies = (stats.bySeverity.high ?? 0) + (stats.bySeverity.critical ?? 0)

    // Calculate events today
    const events = await fetchJson<NormalizedEvent[]>(`${API_BASE_URL}/api/events/recent`)
    const today = new Date()
    today.setHours(0, 0, 0, 0)
    const eventsToday = events.filter(e => new Date(e.timestamp) >= today).length

    return {
      eventsToday: eventsToday || stats.total,
      activePipelines: stats.bySource.github ?? 3, // fallback default
      openAnomalies,
      criticalAlerts,
    }
  } catch (err) {
    console.error("fetchDashboardMetrics failed, falling back to mock", err)
    return {
      eventsToday: 1247,
      activePipelines: 18,
      openAnomalies: 7,
      criticalAlerts: 2,
    }
  }
}

