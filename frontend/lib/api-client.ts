export async function fetchWorkflows(): Promise<WorkflowSuggestion[]> {
  try {
    const data = await fetchJson<any[]>(`${API_BASE_URL}/api/workflow/latest?limit=15`)
    return data.map((item) => ({
      id: item.id,
      anomalyId: item.id,
      title: item.workflow?.title || `Optimize workflow: ${item.metric}`,
      description: item.workflow?.problem || item.rootCause || "Workflow recommendation",
      steps: (item.workflow?.actionPlan || []).map((label: string, idx: number) => ({
        label,
        description: `Action step generated by optimization model.`,
      })),
      createdAt: item.processedAt || item.receivedAt || new Date().toISOString(),
      svg: item.svg,
    }))
  } catch (err) {
    console.error("fetchWorkflows failed", err)
    return []
  }
}

